function bringLogin() {
    let form = document.getElementById('form');
    let top = document.getElementById('top');

    form.style.display = 'none';
    top.style.display = 'none';
}

function bringSignup() {
    let form = document.getElementById('form');
    let top = document.getElementById('top');

    form.style.display = 'none';
    top.style.display = 'none';
}

function bring2Norm() {
    let form = document.getElementById('form');
    let top = document.getElementById('top');

    form.style.display = 'flex';
    top.style.display = 'flex';
}